        <h3>This project was a challenge for myself, to see how much of a game I could make within a weeks spare-time. With all the assets and scripts being made from the ground-up. In this project I experienced and learned about:
<ul>
<li>3D modelling</li>
<li>Texture-painting</li>
<li>Level-design</li>
<li>Game-design</li>
<li>User-interfaces</li>
<li>Child and parent elements</li>
<li>Importing textures and normal-maps</li>
</ul>
The game was a standard tower-defense genre game, where the enemies would be different animals from the forest, who are notorious for stealing food, ergo the animals would chase after your picnic basket filled with food. This spawned the name Picnic TD.<br><br>This project was made in Unity, and everything besides a couple of sound-effects, were made from scratch by myself. This was playtested by a couple of people, giving insight into the level-design and balancing.<br><br>This project was also heavily inspired by the <i>"PixelJunk Monsters"</i> game, as it was both a nostalgic game for me but also an ideal and fun way to implement a tower-defense genre.
