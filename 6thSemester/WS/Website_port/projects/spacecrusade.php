        <h3>
        This project was one of the first relatively big games, compared to being a single developer, that I had thought of. The basic idea was to make an old style arcade shooter, with very simple controls, blasting enemies through different levels, including a boss at the end of every level.<br><br>In this project I learned about:
<ul>
<li>Basic 3D modelling</li>
<li>Waypoints</li>
<li>Vectors</li>
<li>Colliders and trigger events</li>
<li>Insight into how joystick controls are mapped, e.g. the analog sticks and how to make proper code to handle the inputs.</li>
<li>Quaternions and rotation in child-parent objects.</li>
</ul>
This project was made in Unity, with all the 3D models and code made from scratch by myself. The sound effects were taken from free sound repositories on the web, but the soundtrack was my first real experience with making game music single-handedly.<br>I also conducted a small usability test, which made me switch to joystick in the first place. The testing also revealed balancing issues, which resulted in my first reflections and evaluation of the gameplay and balancing.<br><br>Note: This may not work with your joystick.
